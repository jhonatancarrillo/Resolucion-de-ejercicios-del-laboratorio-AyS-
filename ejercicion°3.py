 
#Declara 5 como num_one y 4 como num_two para luego hacer las operaciones

#declaramos las variables

num_one = 5
num_two = 4

# Sumar num_one y num_two
total = num_one + num_two

# Restar num_two de num_one
diff = num_one - num_two

# Multiplicar num_one por num_two
producto = num_one * num_two

# Dividir num_one por num_two
division = num_one / num_two

# Encontrar el módulo (resto) de num_two dividido por num_one
resto = num_two % num_one

# Elevar num_one a la potencia de num_two
exp = num_one ** num_two

# Realizar la división entera (división del piso)
floor_division = num_one // num_two

# Imprimir los resultados
print("Suma:", total)
print("Resta:", diff)
print("Producto:", producto)
print("División:", division)
print("Módulo (resto):", resto)
print("Potencia:", exp)
print("División entera:", floor_division)

 
 
 
 
 
 
 
 